    private void cargarTodo() {
        try {
            Usuarios = GU.gestionListarUsuarios();
            for (int i = 0; i < Usuarios.size(); i++) {
                Usuario usu = Usuarios.get(i);
                agregarFila(usu);
            }
        } catch (Exception e) {
            System.out.println("Error al cargar la tabla");
        }
    }

