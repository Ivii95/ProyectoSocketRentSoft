    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        Formulario f = new Formulario(this, true);
        f.setVisible(true);
        usu = f.getUsuario();
        if (usu != null) {
            GU.gestionInsertarUsuario(usu);
            agregarFila(usu);
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (recogerIdFila() != -1) {

            int idusu = recogerIdFila();
            Usuario usu = null;
            for (int i = 0; i < Usuarios.size(); i++) {
                if (Usuarios.get(i).getId() == idusu) {
                    usu = Usuarios.get(i);
                }
            }
            ModificarUsu mf = new ModificarUsu(this, true);
            mf.setUsuario(usu);
            mf.setVisible(true);
            usu = mf.getUsuario();
            if (usu != null) {
                GU.gestionActualizarUsuario(usu);
                borrarTodo();
                cargarTodo();
            }

        } else {
            JOptionPane.showMessageDialog(this, "Tienes que seleccionar un usuario", "IMPORTANTE", HEIGHT);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        int id = recogerIdFila();
        if (id != -1) {
            GU.gestionBorrarUsuario(id);
            modeloTabla.removeRow(tblPrinci.getSelectedRow());
        } else {
            JOptionPane.showMessageDialog(this, "Tienes que seleccionar un usuario", "IMPORTANTE", HEIGHT);
        }

    }//GEN-LAST:event_btnBorrarActionPerformed

